public class DataTypeExercise {
    public static void main(String[] args) {
        // Match values to datatypes
        double pi = 3.14159;  // decimal number
        char initial = 'A';   // single character
        boolean isTrue = true; // logical value
        long largeNumber = 100000; // whole number

        // Print the values and their types
        System.out.println("pi: " + pi);
        System.out.println("initial: " + initial);
        System.out.println("isTrue: " + isTrue);
        System.out.println("largeNumber: " + largeNumber);
    }
}
