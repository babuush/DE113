public class PromotionRulesExercise {
    public static void main(String[] args) {
        // Match values to datatypes
        byte a = 10;
        int b = 20;
        int out1 = a + b;
        System.out.println(out1);

        short c = 5;
        float d = 3.14f;
        double out2 = c * d;
        System.out.println(out2);

        long e = 1000000000L;
        double f = 3.14159;
        double out3 = e / f;
        System.out.println(out3);


    }

}